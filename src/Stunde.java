public class Stunde {
	private String kurs;
	private String lehrer;
	private String raum;
	
	public String getKurs() {
		return kurs;
	}
	
	public void setKurs(String kurs) {
		this.kurs = kurs;
	}
	
	public String getLehrer() {
		return lehrer;
	}
	
	public void setLehrer(String lehrer) {
		this.lehrer = lehrer;
	}
	
	public String getRaum() {
		return raum;
	}
	
	public void setRaum(String raum) {
		this.raum = raum;
	}
}
